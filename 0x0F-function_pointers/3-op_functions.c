/**
 * op_add - add a and b
 * @a: int
 * @b: int
 *
 * Description: add value a to value b
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract a b
 * @a: int
 * @b: int
 *
 * Description: subtract a from b
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply a by b
 * @a: int
 * @b: int
 *
 * Description: multiply a by b
 * Return: quotient
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide and b
 * @a: int
 * @b: int
 *
 * Description: divide a by b
 * Return: division result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulus
 * @a: int
 * @b: int
 *
 * Description: get remainder
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
